              เด็กบางส่วนที่เรียนที่โรงเรียนธรรมิกวิทยาจำเป็นต้องเข้าร่วมการเรียนรวมในโรงเรียนในละแวกใกล้เคียงเพื่อพัฒนาศักยภาพร่วมกับเด็กทั่วไป
